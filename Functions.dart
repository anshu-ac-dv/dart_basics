void main() {
  add(10, 5);
  sub(10, 5);
  mul(10, 5);
  div(10, 5);
}

void add(int number1, int number2) {
  print(number1 + number2);
}

void sub(int number1, int number2) {
  print(number1 - number2);
}

void mul(int number1, int number2) {
  print(number1 * number2);
}

void div(int number1, int number2) {
  print(number1 / number2);
}
