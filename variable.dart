void main() {
  // This program demonstrates variable declaration and assignment in Dart.
  int number;
  // Declaring an integer variable and assigning a value to it.
  number = 42;
  // Printing the value of the variable.
  print('The number is $number');

  double numberTwo;
  // Declaring a double variable and assigning a value to it.
  numberTwo = 3.14;
  numberTwo = 2.718;
  // Printing the value of the variable.
  print('The float number is $numberTwo');
}
